const mongoose = require('mongoose')

const UserSchema = mongoose.Schema({
    name: String,
    username: String,
    email: {
        type: String,
        required: true,
        unique: true,
        match: /[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*@(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?/
    },
    password: {
        type: String,
        required: true,
    },
    role: String,
    image: String,
    phone : {
        type : String,
    }
})

module.exports = mongoose.model('User', UserSchema)